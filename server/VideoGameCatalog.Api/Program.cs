// Program.cs
// Added:
// - EF Core DbContext registration (GameCatalogDbContext) using ConnectionStrings:GameCatalog
// - DI for IGameDb -> GameDb
// - Controllers support (required for GameHttpController later)

using VideoGameCatalog.Api.Modules.Game;
using VideoGameCatalog.Api.Modules.Game.EfEntities;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
builder.Services.AddOpenApi();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

// Controllers (for GameHttpController)
builder.Services.AddControllers();

// EF Core DbContext (SQL Server)
builder.Services.AddDbContext<GameCatalogDbContext>(options =>
{
    options.UseSqlServer(builder.Configuration.GetConnectionString("GameCatalog"));
});

// Game module DI
builder.Services.AddScoped<IGameDb, GameDb>();
builder.Services.AddScoped<GameBl>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.MapOpenApi();

    app.UseSwaggerUI(options =>
    {
        options.SwaggerEndpoint("/openapi/v1.json", "VideoGameCatalog API");
    });
}

app.UseHttpsRedirection();

// Map controllers (required for attribute-routed controllers)
app.MapControllers();

// You can remove this later (template sample endpoint)
var summaries = new[]
{
    "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
};

app.MapGet("/weatherforecast", () =>
{
    var forecast = Enumerable.Range(1, 5).Select(index =>
        new WeatherForecast
        (
            DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
            Random.Shared.Next(-20, 55),
            summaries[Random.Shared.Next(summaries.Length)]
        ))
        .ToArray();
    return forecast;
})
.WithName("GetWeatherForecast");

app.Run();

record WeatherForecast(DateOnly Date, int TemperatureC, string? Summary)
{
    public int TemperatureF => 32 + (int)(TemperatureC / 0.5556);
}


